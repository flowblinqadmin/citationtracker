/**
 * Backfill executive summaries for all complete sites using Claude Sonnet 4.6.
 * Run: node --env-file=.env.local scripts/rerun-summaries.mjs
 */
import { neon } from "@neondatabase/serverless";

const sql = neon(process.env.DATABASE_URL_UNPOOLED);
const OPENAI_KEY = process.env.OPENAI_API_KEY;

async function callClaude(prompt) {
  const res = await fetch("https://api.openai.com/v1/chat/completions", {
    method: "POST",
    headers: {
      "Authorization": `Bearer ${OPENAI_KEY}`,
      "content-type": "application/json",
    },
    body: JSON.stringify({
      model: "gpt-4o",
      max_tokens: 900,
      messages: [{ role: "user", content: prompt }],
    }),
  });
  const data = await res.json();
  if (!res.ok) throw new Error(data.error?.message ?? JSON.stringify(data));
  return data.choices[0]?.message?.content ?? "";
}

function buildPrompt(domain, scorecard, researchData, crawlPageCount, faqCount, hasStructuredData) {
  const overallScore = scorecard?.overallScore ?? 0;
  const projectedScore = Math.min(100, Math.round(overallScore * 1.35 + 10));
  const criticalPillars = (scorecard?.pillars ?? []).filter(p => p.priority === "critical").map(p => p.pillarName);
  const topImprovements = scorecard?.topThreeImprovements ?? [];

  const competitorStatus = researchData?.competitorGeoStatus ?? [];
  const competitivePosition = researchData?.competitivePosition ?? "";
  const competitorsWithAi = competitorStatus.filter(c => c.hasLlmsTxt || c.hasStructuredData).map(c => c.domain);
  const competitorsBlind = competitorStatus.filter(c => !c.hasLlmsTxt && !c.hasStructuredData).map(c => c.domain);

  return `Write a 3-paragraph executive summary for a GEO audit of ${domain}. The reader is the business owner — not a developer, not a marketer.

DATA
Score: ${overallScore}/100 → projected ${projectedScore}/100 with FlowBlinq
Pages audited: ${crawlPageCount} | Q&A pairs found: ${faqCount}
AI-readable signals: ${hasStructuredData ? "partial" : "none"}
Critical gaps: ${criticalPillars.join(", ") || "none identified"}
Top fixes: ${topImprovements.join("; ")}
Competitors with AI presence: ${competitorsWithAi.length ? competitorsWithAi.join(", ") : "none yet"}
Competitors without: ${competitorsBlind.length ? competitorsBlind.join(", ") : "unknown"}
Position: ${competitivePosition || "not assessed"}

PARAGRAPH 1 — Current state
What ${overallScore}/100 means in plain terms: how often (or rarely) this business shows up when someone asks ChatGPT or Perplexity about their category. Name 1-2 specific gaps. Mention pages audited.

PARAGRAPH 2 — Market shift
Use the competitive data. Who in this space is already set up for AI discovery, who isn't, and what happens to ${domain} if a thinner competitor gets cited first. Frame everything as customers, partnerships, and revenue — not technology.

PARAGRAPH 3 — What FlowBlinq changes
Score moves from ${overallScore} to ~${projectedScore}. Name the 1-2 changes that matter most. Close on timing: the category is open now but won't stay that way.

WRITING RULES

1. Sound like a sharp business consultant talking across a table — not a report generated by software.

2. NEVER use these words or phrases (they are flagged as machine-generated text):
additionally, crucial, delve, emphasize/emphasizing, enduring, enhance/enhancing,
foster/fostering, garner, highlight (as verb), intricate, key (as adjective before role/moment),
landscape (figurative), leverage, nestled, pivotal, profound, rich tapestry, seamlessly,
serves as, showcase/showcasing, stands as, tapestry, testament, transformative,
underscore, valuable, vibrant, in the heart of, it's important to note,
reflects broader, setting the stage, marking/shaping the, indelible mark,
deeply rooted, commitment to, diverse array, evolving landscape, holistic

3. No technical jargon the owner wouldn't use: no "schema," "structured data markup," "llms.txt," "FAQPage," "API," "semantic HTML."

4. Bold the phrases that together tell the whole story on their own. Someone who only reads the bolded text should understand: what the score means, what the competitive risk is, and what FlowBlinq fixes. Aim for 4-6 bold phrases total. Bold specific outcomes and numbers — not adjectives, not company names, not generic nouns.

5. Vary sentence length. Mix short declarative sentences with longer ones. Start some sentences with the subject, others with a clause. Do not start consecutive sentences the same way.

6. No bullet points, no sub-headers, no em-dash pairs used for dramatic emphasis.

7. Open paragraph 2 with a concrete, specific statement — not a sweeping claim about "the industry" or "a shift."

8. Every sentence must be specific to this business and this market. Delete any sentence that could apply to any company in any industry.

EXAMPLE — A fictional coworking space audit (to show tone and style, not content to copy):

"Right now, WorkHaus scores 41 out of 100 on AI discoverability. When a founder in Berlin asks ChatGPT for coworking spaces with dedicated podcast studios, WorkHaus doesn't come up — even though it's one of three spaces in the city that actually has them. We audited 15 pages. The site explains what WorkHaus offers clearly enough for a human reader, but none of that information is structured in a way AI tools can parse and cite. The biggest gap: there's no machine-readable description telling AI what WorkHaus is, what it offers, or where it operates.

Not one coworking brand in Berlin has set this up yet. Zero. That means the first operator to get it right will be the default answer when AI tools recommend where to work. If BerlinDesk or CoLab gets there before WorkHaus, they'll be the name that shows up when a relocating product manager asks Perplexity where to find a desk — and that person never sees WorkHaus at all. They don't bounce. They never arrive. The cost isn't a lost click; it's a member who pays €400/month for two years and never knew you existed.

FlowBlinq moves the score from **41 to roughly 65**. The two changes that matter most: giving every page the right signals so AI can accurately describe what WorkHaus offers, and adding the people behind the space — founders, community managers — so the expertise carries weight in AI responses. Berlin's coworking market is wide open in AI search right now. That won't last past the end of the year."

Now write the summary for ${domain} using the data above. Do not reuse any sentences from the example. Match the tone, specificity, and sentence variation. Write exactly 150 words — count them. Each paragraph should be roughly 50 words. Do not submit fewer than 140 words or more than 160 words.`;
}

const sites = await sql`
  SELECT id, domain, geo_scorecard, research_data, crawl_data, recommendations
  FROM geo_sites
  WHERE pipeline_status = 'complete'
`;

console.warn(`Found ${sites.length} complete sites`);

for (const site of sites) {
  try {
    const scorecard = site.geo_scorecard;
    if (!scorecard) { console.warn(`  SKIP ${site.domain} — no scorecard`); continue; }

    const crawlData = site.crawl_data;
    const crawlPageCount = crawlData?.pages?.length ?? 0;
    const faqCount = (crawlData?.pages ?? []).flatMap(p => p.faqContent ?? []).length;
    const hasStructuredData = (crawlData?.pages ?? []).some(p => p.hasStructuredData);

    console.warn(`  ${site.domain}...`);
    const prompt = buildPrompt(site.domain, scorecard, site.research_data, crawlPageCount, faqCount, hasStructuredData);
    const summary = await callClaude(prompt);

    const existingRecs = site.recommendations ?? {};
    await sql`
      UPDATE geo_sites
      SET executive_summary = ${summary},
          recommendations   = ${JSON.stringify({ ...existingRecs, executiveSummary: summary })},
          updated_at        = NOW()
      WHERE id = ${site.id}
    `;

    console.warn(`  ✓ ${site.domain} (${summary.length} chars)`);
    await new Promise(r => setTimeout(r, 1000));
  } catch (err) {
    console.error(`  ✗ ${site.domain}: ${err.message}`);
  }
}

console.warn("Done.");
